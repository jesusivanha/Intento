package Negocio;

public class IngredientesExtra {
    private int id_pedido;
    private int id_unico_producto;
    private int id_ingrediente;
    private int cantidad;
    
    public int getId_pedido() {
        return id_pedido;
    }
    public void setId_pedido(int id_pedido) {
        this.id_pedido = id_pedido;
    }
    public int getId_unico_producto() {
        return id_unico_producto;
    }
    public void setId_unico_producto(int id_unico_producto) {
        this.id_unico_producto = id_unico_producto;
    }
    public int getId_ingrediente() {
        return id_ingrediente;
    }
    public void setId_ingrediente(int id_ingrediente) {
        this.id_ingrediente = id_ingrediente;
    }
    public int getCantidad() {
        return cantidad;
    }
    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    

}
